package com.biaozhunyuan.tianyi.common.model.form;

import java.io.Serializable;

/**
 * Created by 王安民 on 2017/9/13.
 * 保存时提交的单元格
 */

public class TabCell implements Serializable {
    private String fieldName; //单元格名称
    private String fieldType; //单元格类型
    private String fieldValue; //单元格值


    public TabCell() {

    }

    public TabCell(String fieldName, String fieldType, String fieldValue) {
        this.fieldName = fieldName;
        this.fieldType = fieldType;
        this.fieldValue = fieldValue;
    }

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public String getFieldType() {
        return fieldType;
    }

    public void setFieldType(String fieldType) {
        this.fieldType = fieldType;
    }

    public String getFieldValue() {
        return fieldValue;
    }

    public void setFieldValue(String fieldValue) {
        this.fieldValue = fieldValue;
    }
}
