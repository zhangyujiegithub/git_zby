package com.biaozhunyuan.tianyi.common.model.other;

import android.widget.LinearLayout;

import java.io.Serializable;

/**
 * 功能看板用到的实体类
 */

public class FunctionBoard implements Serializable{
    private LinearLayout layout;
    private int index;
    private String title;
    private int ico;
    private String h5url;
    private String uuid;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getH5Url() {
        return h5url;
    }

    public void setH5Url(String h5Url) {
        this.h5url = h5Url;
    }

    public int getIco() {
        return ico;
    }

    public void setIco(int ico) {
        this.ico = ico;
    }

    public String getFunction() {
        return title;
    }

    public void setFunction(String function) {
        this.title = function;
    }

    public LinearLayout getLayout() {
        return layout;
    }

    public void setLayout(LinearLayout layout) {
        this.layout = layout;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public FunctionBoard(LinearLayout layout, int index, String function) {
        this.layout = layout;
        this.index = index;
        this.title = function;
    }


    public FunctionBoard(String h5Url,int index, String function) {
        this.h5url = h5Url;
        this.index = index;
        this.title = function;
    }

    public FunctionBoard(int index, String function) {
        this.index = index;
        this.title = function;
    }
    public FunctionBoard(){
        super();
    }

}
