package com.biaozhunyuan.tianyi.task;

/**
 * 任务过滤 用到的实体类
 */

public class TaskFilter {

    private String projectId = "";
    private String deparmentId ="";
    private String userId = "";
    private String testType = "";
    private String dictSchedulType = "";
    private String isShowAllHistory = "";
    private String status = "";
    private String index = "taskList";

    public TaskFilter(String projectId, String deparmentId, String dictSchedulType) {
        this.projectId = projectId;
        this.deparmentId = deparmentId;
        this.dictSchedulType = dictSchedulType;
    }

    public String getProjectId() {
        return projectId;
    }

    public void setProjectId(String projectId) {
        this.projectId = projectId;
    }

    public String getDeparmentId() {
        return deparmentId;
    }

    public void setDeparmentId(String deparmentId) {
        this.deparmentId = deparmentId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getDictSchedulType() {
        return dictSchedulType;
    }

    public void setDictSchedulType(String dictSchedulType) {
        this.dictSchedulType = dictSchedulType;
    }

    public String getIsShowAllHistory() {
        return isShowAllHistory;
    }

    public void setIsShowAllHistory(String isShowAllHistory) {
        this.isShowAllHistory = isShowAllHistory;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

}
