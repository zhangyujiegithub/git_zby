package com.biaozhunyuan.tianyi.attendance;

import java.io.Serializable;

/**
 * Created by 王安民 on 2017/8/24.
 * 考勤实体类
 */

public class Attendance implements Serializable {
    private String uuid;
    private String creatorId;           //创建人
    private String creationDepartmentId;//创建部门
    private String creationTime;        //创建时间
    private String lastUpdateTime;      //最后更新时间
    private String checkInTime;         //上班时间
    private String checkOutTime;        //下班时间
    private String attendanceDate;      //考勤日期
    private String startWorkTime;       //规定上班时间
    private String endWorkTime;         //规定下班时间
    private String status;              //状态-正常, 出差，加班，请假，调休
    private boolean isComeLate;          //是否迟到
    private boolean isLeaveEarly;        //是否早退
    private String remark;              //备注
    private String overtime;            //加班时间，以分钟为单位
    private String isWorkday;           //是否工作日,（法定节假日调休，如果周六日上班，也算是工作日）
    private String validationStatus;    //部门确认状态，未提交，确认中，已确认
    private String comeLateMinute;      //迟到分钟数
    private String leaveEarlyMinute;    //早退分钟数


    public boolean isComeLate() {
        return isComeLate;
    }

    public void setComeLate(boolean comeLate) {
        isComeLate = comeLate;
    }

    public boolean isLeaveEarly() {
        return isLeaveEarly;
    }

    public void setLeaveEarly(boolean leaveEarly) {
        isLeaveEarly = leaveEarly;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getCreatorId() {
        return creatorId;
    }

    public void setCreatorId(String creatorId) {
        this.creatorId = creatorId;
    }

    public String getCreationDepartmentId() {
        return creationDepartmentId;
    }

    public void setCreationDepartmentId(String creationDepartmentId) {
        this.creationDepartmentId = creationDepartmentId;
    }

    public String getCreationTime() {
        return creationTime;
    }

    public void setCreationTime(String creationTime) {
        this.creationTime = creationTime;
    }

    public String getLastUpdateTime() {
        return lastUpdateTime;
    }

    public void setLastUpdateTime(String lastUpdateTime) {
        this.lastUpdateTime = lastUpdateTime;
    }

    public String getCheckInTime() {
        return checkInTime;
    }

    public void setCheckInTime(String checkInTime) {
        this.checkInTime = checkInTime;
    }

    public String getCheckOutTime() {
        return checkOutTime;
    }

    public void setCheckOutTime(String checkOutTime) {
        this.checkOutTime = checkOutTime;
    }

    public String getAttendanceDate() {
        return attendanceDate;
    }

    public void setAttendanceDate(String attendanceDate) {
        this.attendanceDate = attendanceDate;
    }

    public String getStartWorkTime() {
        return startWorkTime;
    }

    public void setStartWorkTime(String startWorkTime) {
        this.startWorkTime = startWorkTime;
    }

    public String getEndWorkTime() {
        return endWorkTime;
    }

    public void setEndWorkTime(String endWorkTime) {
        this.endWorkTime = endWorkTime;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }



    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getOvertime() {
        return overtime;
    }

    public void setOvertime(String overtime) {
        this.overtime = overtime;
    }

    public String getIsWorkday() {
        return isWorkday;
    }

    public void setIsWorkday(String isWorkday) {
        this.isWorkday = isWorkday;
    }

    public String getValidationStatus() {
        return validationStatus;
    }

    public void setValidationStatus(String validationStatus) {
        this.validationStatus = validationStatus;
    }

    public String getComeLateMinute() {
        return comeLateMinute;
    }

    public void setComeLateMinute(String comeLateMinute) {
        this.comeLateMinute = comeLateMinute;
    }

    public String getLeaveEarlyMinute() {
        return leaveEarlyMinute;
    }

    public void setLeaveEarlyMinute(String leaveEarlyMinute) {
        this.leaveEarlyMinute = leaveEarlyMinute;
    }
}