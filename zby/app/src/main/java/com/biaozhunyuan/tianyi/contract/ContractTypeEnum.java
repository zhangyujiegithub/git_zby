public enum ContractTypeEnum {

    新增("新增", "46155ec539ad4e59a0490d9497b8e69c"),
    转让("转让", "55af99d2f4394633a2dc27fd4f5835d9"),
    受让("受让", "60670b3bbba844269818e0981c460c77"),
    追加("追加", "767de362a7cf40ec805f7c60915c2348"),
    续投("续投", "cac5112a0af24a1094e6aaeb6118ce30");

    private String name;
    private String uuid;

    ContractTypeEnum(String name, String uuid) {
        this.name = name;
        this.uuid = uuid;
    }

    public String getUuid() {
        return this.uuid;
    }
}
