package com.biaozhunyuan.tianyi.attch;

/**
 * 附件类型
 * 
 * @author K
 * 
 */
public enum EnumAttachType {

	// 枚举成员变量，默认是静态

	附件号(0, "附件号"), 本地路径(2, "本地路径"), 新建(3, "新建");

	// 私有成员变量，保存名称
	private int value;
	private String name;

	public int getValue() {
		return value;
	}

	public String getName() {
		return name;
	}

	// 带参构造函数
	EnumAttachType(int value, String name) {
		this.value = value;
		this.name = name;
	}

}
