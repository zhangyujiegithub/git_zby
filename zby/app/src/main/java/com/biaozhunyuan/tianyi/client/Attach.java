package com.biaozhunyuan.tianyi.client;

import java.io.Serializable;

/**
 * 客户往来文件
 */

public class Attach implements Serializable{
    private String uuid;
    private String name;
    private String createTime;
    private String attachments;
    private String customerId;
    private String customerName;
    private String creatorId;
    private Boolean isNotCorrespondenceFile;
    private String workflowId;

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getAttachments() {
        return attachments;
    }

    public void setAttachments(String attachments) {
        this.attachments = attachments;
    }

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public String getCreatorId() {
        return creatorId;
    }

    public void setCreatorId(String creatorId) {
        this.creatorId = creatorId;
    }

    public Boolean getNotCorrespondenceFile() {
        return isNotCorrespondenceFile;
    }

    public void setNotCorrespondenceFile(Boolean notCorrespondenceFile) {
        isNotCorrespondenceFile = notCorrespondenceFile;
    }

    public String getWorkflowId() {
        return workflowId;
    }

    public void setWorkflowId(String workflowId) {
        this.workflowId = workflowId;
    }
}
