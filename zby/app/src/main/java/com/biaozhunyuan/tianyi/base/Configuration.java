package com.biaozhunyuan.tianyi.base;

/**
 * 配置文件用到的实体类
 */

public class Configuration {
    private String stage;
    private String content;
    private String contactTime;
    private String contactTitle;
    private String nextContactTime;
    private String nextContactContent;
    private String printShowName;

    public String getPrintShowName() {
        return printShowName;
    }

    public void setPrintShowName(String printShowName) {
        this.printShowName = printShowName;
    }

    public String getNextContactTime() {
        return nextContactTime;
    }

    public void setNextContactTime(String nextContactTime) {
        this.nextContactTime = nextContactTime;
    }

    public String getNextContactContent() {
        return nextContactContent;
    }

    public void setNextContactContent(String nextContactContent) {
        this.nextContactContent = nextContactContent;
    }

    public String getContactTitle() {
        return contactTitle;
    }

    public void setContactTitle(String contactTitle) {
        this.contactTitle = contactTitle;
    }

    public String getContactTime() {
        return contactTime;
    }

    public void setContactTime(String contactTime) {
        this.contactTime = contactTime;
    }

    public String getStage() {
        return stage;
    }

    public void setStage(String stage) {
        this.stage = stage;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
